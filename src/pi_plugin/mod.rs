    #[test]
    fn test_console_mode_wires_settings_idempotently() {
        let cache = TempDir::new().unwrap();
        let home = TempDir::new().unwrap();
        let rp = ensure_pi_plugin_at(
            EnsureMode::Console,
            (Source::Cache, None),
            cache.path(),
            home.path(),
        )
        .unwrap();

        let settings = pi_settings_path(home.path());
        let text1 = std::fs::read_to_string(&settings).unwrap();
        let val1: serde_json::Value = serde_json::from_str(&text1).unwrap();
        let exts = val1["extensions"].as_array().unwrap();
        assert_eq!(exts.len(), 1);
        assert_eq!(exts[0].as_str().unwrap(), rp.dist_entry.to_string_lossy());

        // Re-run: byte-identical (pure no-op the second time).
        let rp2 = ensure_pi_plugin_at(
            EnsureMode::Console,
            (Source::Cache, None),
            cache.path(),
            home.path(),
        )
        .unwrap();
        assert_eq!(rp.dist_entry, rp2.dist_entry);
        let text2 = std::fs::read_to_string(&settings).unwrap();
        assert_eq!(text1, text2, "second Console run must be byte-identical");
    }

    #[test]
    fn test_console_settings_preserves_user_entries_and_replaces_stale_wg() {
        let home = TempDir::new().unwrap();
        let settings = pi_settings_path(home.path());
        std::fs::create_dir_all(settings.parent().unwrap()).unwrap();
        // A user extension + a STALE wg cache entry from an older version.
        std::fs::write(
            &settings,
            serde_json::to_string_pretty(&serde_json::json!({
                "packages": ["npm:pi-web-access@1.0.0"],
                "extensions": [
                    "/home/u/my-ext/index.ts",
                    "/home/u/.cache/wg/pi-plugin/0.0.1/dist/index.js"
                ]
            }))
            .unwrap(),
        )
        .unwrap();

        let new_entry = PathBuf::from("/home/u/.cache/wg/pi-plugin/0.1.0/dist/index.js");
        let changed = upsert_console_settings(&settings, &new_entry).unwrap();
        assert!(changed);

        let val: serde_json::Value =
            serde_json::from_str(&std::fs::read_to_string(&settings).unwrap()).unwrap();
        let exts: Vec<&str> = val["extensions"]
            .as_array()
            .unwrap()
            .iter()
            .map(|v| v.as_str().unwrap())
            .collect();
        // User entry preserved; stale wg entry replaced by the new one; packages intact.
        assert_eq!(
            exts,
            vec![
                "/home/u/my-ext/index.ts",
                "/home/u/.cache/wg/pi-plugin/0.1.0/dist/index.js"
            ]
        );
        assert_eq!(
            val["packages"][0].as_str().unwrap(),
            "npm:pi-web-access@1.0.0"
        );

        // Idempotent: a second upsert with the same entry is a no-op.
        assert!(!upsert_console_settings(&settings, &new_entry).unwrap());
    }

